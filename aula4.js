// Operadores JAVASCRIPT

// + = ADIÇÃO
// - = SUBTRAÇÃO
// / = DIVISÃO
// * = MULTIPLICAÇÃO

const resultado = 7 + 3;
console.log(resultado);

const resultadoSubtracao = 9 - 5;
console.log(resultadoSubtracao);

const resultadoDivisao = 25 / 5;
console.log(resultadoDivisao)

const resultadoMutiplicacao = 8 * 8;
console.log(resultadoMutiplicacao);

// Podemos usar esses operadores em outras situações, como inrementar uma frase por exemplo.


